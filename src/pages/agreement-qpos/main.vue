  <h2>三、服务费用及账务处理：</h2>

  <p>3.1本条款项下的结算货币为人民币。</p>

  <p>3.2乙方应向支付合作方支付的交易手续费计算方式为：交易额乘以交易费率。甲方根据与支付合作方的约定参与交易手续费的处理。</p>

  <p>3.3银行卡交易成功以终端显示屏显示“交易成功”并且成功打印出交易凭条，并由持卡人签字确认为准。</p>

  <p>3.4银联卡交易手续费在交易本金中进行清分。交易资金一般在交易完成后1个工作日到账，遇周六日及国家法定节假日清算时间顺延至下一个工作日。交易手续费率标准以银联标准费率为基准。如遇银联或支付合作方调整交易手续费率，本条款项下的交易手续费率将相应调整，甲方对此不承担责任。</p>

  <p>3.5乙方银联卡交易净金额（已扣除本条款规定的交易手续费）在乙方消费支付交易成功后（以终端显示屏显示“交易成功”并交持卡人签字为准），由清算机构划拨至乙方提交给甲方和支付合作方的账户中，清算数据以转接机构或发卡机构的交易电子数据为准。</p>

  <p>3.6若乙方需变更账户，应提前7个工作日以书面方式加盖公章后正式通知甲方和支付合作方，甲方和支付合作方将乙方提出的变更时间和新的银行账户提交至清算机构更改系统注册，并从书面约定的时间起按新的账户划拨乙方银行卡交易资金。</p>

  <p>3.7若乙方需要变更商户地址、联系方式等信息要素，需提前7个工作日以书面形式加盖公章后正式通知甲方，甲方将乙方提出的变更信息提交至支付合作方和清算机构，在系统中重新注册更新，并进行终端的变更。</p>

