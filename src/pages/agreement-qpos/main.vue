  <h1>QPOS支付解决方案条款</h1>

  <p>本QPOS支付解决方案条款（“本条款”）为钱方好近商户服务协议（“服务协议”）的附件，如服务协议的乙方开通QPOS支付解决方案，则本条款自动适用。本条款未约定事宜，均根据服务协议执行。</p>

