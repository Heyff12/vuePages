  <p>1.5甲方、支付合作方或其授权机构提供终端设备的维护服务。</p>

  <h2>二、关于受理银行卡业务的约定</h2>

  <p>2.1乙方同意按照服务协议和本条款的约定申请开通受理银联卡业务，并承诺遵守银联卡相关的各项规章制度。</p>

  <p>2.2乙方收银人员在受理银联卡时，需按照《QPOS用户手册》以及支付合作方的具体指示进行终端操作，应仔细核对签购单上持卡人亲笔签名与银行卡背面的预留签名是否一致，如发现卡片有伪造痕迹，或其他任何疑问应及时与甲方及支付合作方联系。核验无误后，请持卡人在手机上的签购单上签字，确认交易金额是否正确。乙方未按照要求执行而导致的经济损失，由乙方自行承担。</p>

  <p>2.3为避免持卡人进行的欺诈交易为双方带来损失，乙方应对包括但不限于下列异常交易情况特别关注，及时与甲方联系：</p>

  <p>（1）卡面凸印或平印卡号有更改痕迹，或终端机具显示卡号与凸印或平印卡号不符；</p>

  <p>（2）持卡人未经选择即购买高价值或易变现商品；</p>

  <p>（3）持卡人购买多件同一类或同一规格商品；</p>

  <p>（4）持卡人不清楚本卡余额或可用信用额度，密码等，反复要求试刷卡；</p>

